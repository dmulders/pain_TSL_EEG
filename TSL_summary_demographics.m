function TSL_summary_demographics( )
% Compute the average demographics for the TSL experiment, after 
% excluding participants. 


all_subj_idx        = setdiff(1:36,[1, 11, 15, 28, 33]) ; 
% --------------------------------------------------------- % 
% subj 1: 500ms stim
% subj 11: difficulty to feel cool (pre-check mistake)
% subj 15 + 33: fell asleep
% subj 28: post-check mistake 
% --------------------------------------------------------- %

n_subj = length(all_subj_idx) ; 

all_ages = [21, 22, 28, 20, 22, 22, ...
    24, 21, 27, 20, 25, 23, ...
    22, 27, 39, 22, 23, 20, ...
    24, 26, 26, 18, 20, 28, ...
    30, 25, 26, 24, 25, 23, ...
    18, 21, 52, 27, 24, 18] ; 
all_ages = all_ages(all_subj_idx) ;
n_ages = length(all_ages) ; 
disp(['=*= Mean ([Min, Max]) age: ', num2str(mean(all_ages)), ...
    '([',num2str(min(all_ages)),',',num2str(max(all_ages)),'])']) ; 

all_hands = {'R', 'R', 'R', 'R', 'L', 'R', ...
    'R', 'R', 'L', 'R', 'R', 'R', ...
    'R', 'R', 'R', 'R', 'R', 'R', ...
    'R', 'R', 'L', 'R', 'R', 'R', ...
    'R', 'R', 'R', 'R', 'L', 'R', ...
    'R', 'R', 'R', 'R', 'R', 'R'} ; 
all_hands = all_hands(all_subj_idx) ; 
n_hands = length(all_hands) ; 
n_R = sum( cellfun(@(x) strcmpi(x, 'R'), all_hands) ) ; 
disp(['=*= Nb of right-handed subjects: ', num2str(n_R), '/', num2str(n_hands)]) ;

all_genders = {'F', 'F', 'F', 'F', 'M', 'F',...
    'M', 'M', 'F', 'M', 'M', 'F',...
    'M', 'M', 'M', 'F', 'F', 'M',... 
    'F', 'M', 'F', 'M', 'F', 'F',...
    'M', 'M', 'M', 'F', 'M', 'M',... 
    'F', 'F', 'F', 'F', 'F', 'M'} ; 
all_genders = all_genders(all_subj_idx) ; 
n_genders = length(all_genders) ; 
n_F = sum( cellfun(@(x) strcmpi(x, 'F'), all_genders) ) ; 
disp(['=*= Nb of females: ', num2str(n_F), '/', num2str(n_genders)]) ;

% A-delta threshold based on staircase procedure using reaction times
% when RT was slower than 650ms ==> no Adelta resp, cfr Mouraux2012
% Threshold = mean of 4 temp which lead to 3 consecutive changes
all_thresholds = [49.95, 49.975, 54, 55.15, 49.85, 44, ...
    46.7, 54, 53, 53.7, 55.9, 53, ...
    56.45, 59.97, 56.15, 58, 53, 58, ...
    48, 56, 41.85, 57.05, 56.9, 58, ...
    59.725, 51.975, 51.95, 54.75, 42.975, 58.55, ...
    53, 50.75, 47.15, 40.15, 51.975, 56.75] ; % sub01: 500ms used
all_thresholds = all_thresholds(all_subj_idx) ;
n_thresh = length(all_thresholds) ; 
n_above_58 = sum(all_thresholds>58) ; 
disp(['=*= Mean (std, [Min, Max]) threshold: ', num2str(mean(all_thresholds)), ...
    '(',num2str(std(all_thresholds)),' ,[',num2str(min(all_thresholds)),',',num2str(max(all_thresholds)),'], ',num2str(n_above_58),' >58)']) ; 

all_I1 = [15, 15, 15, 15, 15, 15, ...
    15, 15, 15, 15, 20, 15, ...
    15, 15, 15, 15, 15, 15, ...
    15, 15, 15, 15, 15, 15, ...
    15, 15, 15, 15, 20, 15, ...
    20, 15, 15, 15, 20, 20] ; 

all_I2 = [51, 58, 58, 58, 58, 57, ...
    58, 58, 58, 58, 58, 58, ...
    58, 58, 58, 58, 58, 58, ...
    57, 57, 58, 58, 58, 58, ...
    57, 57, 57, 57, 58, 58, ...
    58, 57, 57, 57, 57, 57] ; 


disp(['-- Nb of participants: ', num2str(n_subj), ', nb of ages: ', num2str(n_ages), ...
    ', nb of handedness: ', num2str(n_hands), ', nb of genders: ', num2str(n_genders)]) ; 


end

